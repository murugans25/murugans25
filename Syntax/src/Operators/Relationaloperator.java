package Operators;
//comparsion operator
/*   these are relational operators symbols
       ==    equal to
       !=    not equal to
       >     greater than
       <
       >=
       <=

 */
public class Relationaloperator {
    public static void main(String []args){
        int a=10,b=20;
                //( ==,!=,<,>,<=,>=) these condition are check in these operators
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a>b);

    }
}
