package looop;
//while is repeats a block of code as long as a specified condition is true.
public class whileloop {
    public static void main(String[]args){
        int i=1;
        while (i<5){
            System.out.println("hello");
            i++;
        }
        System.out.println("************************************************");

        int j=1;
        while (j<=10){
            System.out.println(j);
            j++;
        }
    }
}
